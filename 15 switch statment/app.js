switch (true) {
    case (num > 90):
        console.log("Grade: A");
        break;
    case (num > 80):
        console.log("Grade: B");
        break;
    case (num > 70):
        console.log("Grade: C");
        break;
    case (num > 60):
        console.log("Grade: D");
        break;
    case (num > 50):
        console.log("Grade: E");
        break;
    default:
        console.log("Grade: F");
        break;
}
